     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        if (Request::has('itemsPerPage') && Request::get('itemsPerPage') == -1) {
            $per_page = 99999999;
        } else {
            $per_page = Request::get('itemsPerPage') ?? 10;
        }
        return Inertia::render('Users/Index', [
            'filters' => Request::all('search'),
            'users' => User::sortBy(Request::only('sortBy', 'sortDesc'))
                ->filter(Request::only('search'))
                ->paginate($per_page)
        ]);
    }

